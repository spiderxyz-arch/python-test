phrase = "  Hey This is Just a String  "
print(phrase.strip())  #this will reduce the extra space
print(phrase.split())  #this will split the words
print(phrase.count("i"))  #this will count the string
print(phrase.isalnum())   #this will check the string if there has no space or have if have it will show false if not then True
print(phrase.title())     #this wil  tell you that your String start with capital letter
